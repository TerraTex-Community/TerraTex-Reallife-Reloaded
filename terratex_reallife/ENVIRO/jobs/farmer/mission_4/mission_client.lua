addEvent("startFarmMission_4", true)
function startFarmMission_4_func(tractor, anha)
    farmerJobExplodeDeleteTable = {}
    setTimer(sync_coolib, 500, 1, tractor, anha)
    duengerb = 0
    for theKey, thePaar in ipairs(duengermarkerb) do
        local marker = createMarker(thePaar[1], thePaar[2], thePaar[3], "checkpoint", 2.0, 0, 255, 0)
        addEventHandler("onClientMarkerHit", marker, hitDuengenb, false)
        table.insert(farmerJobExplodeDeleteTable, marker)
    end
end

addEventHandler("startFarmMission_4", getRootElement(), startFarmMission_4_func)

function sync_coolib(ele, eleb)
    if (isElement(ele)) then
        for theKey, theCar in ipairs(getElementsByType("vehicle")) do
            if (isNotFarmingHitable[getElementModel(theCar)]) then
                setElementCollidableWith(ele, theCar, false)
                setElementCollidableWith(eleb, theCar, false)
            end
        end
        setTimer(sync_coolib, 500, 1, ele, eleb)
    end
end

function hitDuengenb(playa)
    if (playa == getLocalPlayer()) then
        local istractor = false
        if (isPedInVehicle(getLocalPlayer())) then
            if (getElementModel(getPedOccupiedVehicle(getLocalPlayer())) == 531) then
                istractor = true
            end
        end
        if not (istractor) then
            outputChatBox("Pflügen kann man nur mit dem Tractor!", 255, 0, 0)
        else
            if isElement(source) then
                destroyElement(source)
            end
            duengerb = duengerb + 1
            --setElementFrozen(playa,true)
            local finished = false
            if (duengerb >= (table.getn(duengermarkerb) / 4)) then
                finished = true
                triggerServerEvent("finish_farmer_mission_4", getLocalPlayer())
            end
        end
    end
end
