function createTrains_func()
    if (trainCounter < 2) then -- normally 4
        local rand = math.random(0, 1)
        local trainID = 537 + rand

        local marker = math.floor(#trainPortPoints / #traintraillers * trainCounter)

        if (marker > #trainPortPoints) then marker = #trainPortPoints -1; end
        if (marker == 0) then marker = 1; end

        local coord = trainPortPoints[marker];
        trains[trainCounter + 1] = createVehicle(trainID, coord[1], coord[2], coord[3], 0, 0, 0, "Zug", true)
        setTrainDerailable(trains[trainCounter + 1], false)
        setTrainDirection(trains[trainCounter + 1], true)
        vioSetElementData(trains[trainCounter + 1], "lastSnycMark", marker)

        local prefTrailer = createVehicle(trailers[math.random(1, table.getSize(trailers))], trainPortPoints[marker][1], trainPortPoints[marker][2], trainPortPoints[marker][3])
        attachTrailerToVehicle(trains[trainCounter + 1], prefTrailer)

        for n = 0, traintraillers[trainCounter + 1], 1 do
            local newTrailer = createVehicle(trailers[math.random(1, table.getSize(trailers))], trainPortPoints[marker][1], trainPortPoints[marker][2], trainPortPoints[marker][3])
            attachTrailerToVehicle(prefTrailer, newTrailer)
            prefTrailer = newTrailer
        end

        setTrainSpeed(trains[trainCounter + 1], trainSpeedSave[trainCounter + 1])
        setTimer(resetTrainSpeed, (traintimer[trainCounter + 1]), 0, (trainCounter + 1))
        trainCounter = trainCounter + 1
        createTrains_func()
    end
end
addEventHandler("onResourceStart", getResourceRootElement(getThisResource()), createTrans_Timer_Func)

function isPlayerNearTrain(theTrainID)
    local x, y, z = getElementPosition(trains[theTrainID])
    local isPlayerInCol = 0
    for theKey, thePlayer in ipairs(getElementsByType("player")) do
        local px, py, pz = getElementPosition(thePlayer)
        if (getDistanceBetweenPoints3D(px, py, pz, x, y, z) < 80) then
            isPlayerInCol = isPlayerInCol + 1
        end
    end
    return isPlayerInCol
end

function resetTrainSpeed(theTrainID)
    for anotherid, theTrainElement in ipairs(trains) do
        setTrainSpeed(trains[anotherid], trainSpeedSave[anotherid])
    end
    setTrainSpeed(trains[theTrainID], trainSpeedSave[theTrainID])
    if (not (getElementSyncer(trains[theTrainID])) or isPlayerNearTrain(theTrainID) == 0) and not (isPlayerNearTrain(theTrainID) > 0) then

        if not (vioGetElementData(trains[theTrainID], "sync")) then

            local x, y, z = getElementPosition(trains[theTrainID])
            local smalesDis = 800
            local smalesDisMark = 0
            for theKey, theTable in ipairs(trainPortPoints) do
                if (getDistanceBetweenPoints3D(x, y, z, theTable[1], theTable[2], theTable[3]) < smalesDis) then
                    smalesDis = getDistanceBetweenPoints3D(x, y, z, theTable[1], theTable[2], theTable[3])
                    smalesDisMark = theKey + 1
                    if not (trainPortPoints[smalesDisMark]) then
                        smalesDisMark = 1
                    end
                end
            end
            vioSetElementData(trains[theTrainID], "lastSnycMark", smalesDisMark)
        end
        --outputDebugString(theTrainID.." "..smalesDisMark)
        vioSetElementData(trains[theTrainID], "lastSnycMark", vioGetElementData(trains[theTrainID], "lastSnycMark") + 1)
        local lastMark = vioGetElementData(trains[theTrainID], "lastSnycMark")
        if (trainPortPoints[lastMark]) then
            if (trainPortPoints[lastMark][1] == -1 and trainPortPoints[lastMark][2] == -1 and trainPortPoints[lastMark][3] == -1) then
                lastMark = 1
                vioSetElementData(trains[theTrainID], "lastSnycMark", 1)
            end
        else
            lastMark = 1
            vioSetElementData(trains[theTrainID], "lastSnycMark", 1)
        end
        setElementPosition(trains[theTrainID], trainPortPoints[lastMark][1], trainPortPoints[lastMark][2], trainPortPoints[lastMark][3])
        vioSetElementData(trains[theTrainID], "sync", true)
    else
        vioSetElementData(trains[theTrainID], "sync", false)
    end
    for theKey, theTable in ipairs(trainDropPoints) do
        local x, y, z = getElementPosition(trains[theTrainID])
        if (getDistanceBetweenPoints3D(x, y, z, theTable[1], theTable[2], theTable[3]) < 30) then
            if not (theTable[4]) then
                trainDropPoints[theKey][4] = true
                if (math.random(1, 6) == 2) then
                    trainDropPoints[theKey][4] = createPickup(theTable[1], theTable[2], theTable[3], 3, trainSpawnItem)
                    local hasItem = false
                    while (not (hasItem)) do
                        local itemTABID = math.random(1, table.getn(trainDropItemList))
                        local spawnWahr = math.random(0, 100) / 100
                        if (spawnWahr <= trainDropItemList[itemTABID][3]) and not (hasItem) then
                            hasItem = true
                            vioSetElementData(trainDropPoints[theKey][4], "dropItemSettings", { trainDropItemList[itemTABID][1], trainDropItemList[itemTABID][2], theKey })
                        end
                    end
                    addEventHandler("onPickupHit", trainDropPoints[theKey][4], pickupItemsByTrainDrop, false)

                else
                    setTimer(resetItemSpawning, trainSpawnItemWaitTime, 1, theKey)
                end
            end
        end
    end
end

