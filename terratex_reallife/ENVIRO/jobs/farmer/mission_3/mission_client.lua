addEvent("startFarmMission_3",true)
function startFarmMission_3_func(tractor)
	farmerJobExplodeDeleteTable={}
	setTimer(sync_cooli,500,1,tractor)
	duenger=0
	for theKey,thePaar in ipairs(duengermarker)do
		local marker=createMarker ( thePaar[1],thePaar[2],thePaar[3], "checkpoint", 2.0, 0, 255, 0)
		addEventHandler("onClientMarkerHit",marker,hitDuengen,false)
		table.insert(farmerJobExplodeDeleteTable,marker)
	end
end
addEventHandler("startFarmMission_3",getRootElement(),startFarmMission_3_func)

isNotFarmingHitable={[610]=true,[531]=true,[478]=true,[455]=true}
function sync_cooli(ele)
	if(isElement(ele))then
		for theKey,theCar in ipairs(getElementsByType("vehicle"))do
			if(isNotFarmingHitable[getElementModel(theCar)])then
				setElementCollidableWith ( ele, theCar, false )
			end
		end
		setTimer(sync_cooli,500,1,ele)
	end
end

function hitDuengen(playa)
	if(playa==getLocalPlayer())then
		local istractor=false
		if(isPedInVehicle(getLocalPlayer()))then
			if(getElementModel(getPedOccupiedVehicle(getLocalPlayer()))==531)then
				istractor=true
			end
		end
		if not(istractor)then
			outputChatBox("Duengen kann man nur mit dem Tractor!",255,0,0)
		else
			destroyElement(source)
			duenger=duenger+1
			--setElementFrozen(playa,true)
			local finished=false
			if(duenger==table.getn(duengermarker))then
				finished=true				
				triggerServerEvent("finish_farmer_mission_3",getLocalPlayer())
			end			
		end
	end
end










